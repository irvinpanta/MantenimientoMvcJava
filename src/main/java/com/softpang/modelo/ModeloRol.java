
package com.softpang.modelo;

public class ModeloRol {
    
    private Integer xFlag;
    private String xRol;
    private String xDescripcion ;
    private Integer activo;
    
    
    public ModeloRol(){}

    public Integer getxFlag() {
        return xFlag;
    }

    public void setxFlag(Integer xFlag) {
        this.xFlag = xFlag;
    }
    

    public String getxRol() {
        return xRol;
    }

    public void setxRol(String xRol) {
        this.xRol = xRol;
    }

    public String getxDescripcion() {
        return xDescripcion;
    }

    public void setxDescripcion(String xDescripcion) {
        this.xDescripcion = xDescripcion;
    }

    public Integer getActivo() {
        return activo;
    }

    public void setActivo(Integer activo) {
        this.activo = activo;
    }
    
    
    
}
